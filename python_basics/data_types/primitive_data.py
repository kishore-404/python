# The type is inferred at runtime based on the value.


# A primitive data type is a basic, predefined data type provided by a programming language

# Key Points:
# Primitive types store a single value.
# They are immutable in Python (their value cannot change after creation).
# They serve as the foundation for more complex or user-defined data types.



# Integer
age = 25
print("Integer:", age, type(age))

# Float
price = 19.99
print("Float:", price, type(price))

# Boolean
is_student = True
print("Boolean:", is_student, type(is_student))

# String
name = "Alice"
print("String:", name, type(name))

# NoneType
nothing = None
print("NoneType:", nothing, type(nothing))
